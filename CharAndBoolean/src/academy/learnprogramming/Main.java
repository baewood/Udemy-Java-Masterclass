// Lesson on char and boolean Primitive Data Types

package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {

        char myChar = 'D'; // character literal
        char myUnicodeChar = '\u0044'; // Unicode for D
        System.out.println(myChar);
        System.out.println(myUnicodeChar);

        char myCopyrightChar = '\u00A9';
        System.out.println(myCopyrightChar);

        boolean myTrueBoolValue = true;
        boolean myFalseBoolValue = false;

        boolean isCustomerOverTwentyOne = true; // Just an example of how to write the syntax
    }
}
